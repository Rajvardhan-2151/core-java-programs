package iterationBranching;

// for loop is an entry control loop. the condition must be strictly satisfied to enter into the loop
// demonstration of for loop in java
public class forLoop {
	public static void main(String[] args) {
		int n = 10;
		for (int i = 1; i <= 10; i++) {
			System.out.print(n + " * " + i + " = " + n * i + "\t");
		}
	}
}
